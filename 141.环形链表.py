# @lc code=start
# Definition for singly-linked list.
# class ListNode:
#     def __init__(self, x):
#         self.val = x
#         self.next = None

class Solution:
    def hasCycle(self, head: ListNode) -> bool:
        # 20/20 cases passed (64 ms)
        # Your runtime beats 50.11 % of python3 submissions
        # Your memory usage beats 87.17 % of python3 submissions (18.1 MB)
        slow, fast = head, head
        while fast and fast.next:
            slow = slow.next
            fast = fast.next.next
            if slow == fast:
                return True
        return False
# @lc code=end

